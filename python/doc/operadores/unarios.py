# Operadores Unários

a = 3   # Inteiro

a += 1  # Em outras linguagens essa atribuição equivale a a++(Pós-Fixada)
print(a)

print(not False) # Retorna Verdadeiro
print(not True) # Retorna Falso
print(not not True) # Retorna Verdadeiro
print(not not False) # Retorna Falso

b = 3 # Inteiro Positivo
print(-a) # Retorna inteiro Negativo

c = -3 # Inteiro Negativo
print(-c) # Retorna inteiro Positivo

