# Operadores Relacionais

x = 7 # Tipo Inteiro
y = 5 # Tipo Inteiro

print(x > y) # x é maior que Y?
print(x >= y) # x é maior ou Igual a Y?
print(x < y) # x é menor que y?
print(x <= y) # x é menor ou igual a y?
print(x == y) # x é igual a y?
print(x != y) # x Diferente de y?
print(x == 'x') # X Inteiro é igual a x String?

print('5' != 5) # Verifica que o primeiro 5 é do tipo String e o segundo 5 é Inteiro